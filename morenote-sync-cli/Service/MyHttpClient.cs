        public static async Task<string> Bearer(string url, string token)
        {
            string str;
            using (HttpClient httpClient = new HttpClient((HttpMessageHandler)new HttpClientHandler()
            {
                ServerCertificateCustomValidationCallback = (message, cert, chain, error) => true
            }))
            {
                httpClient.DefaultRequestHeaders.Authorization = new AuthenticationHeaderValue(nameof(Bearer), token);
                str = await (await httpClient.GetAsync(url)).Content.ReadAsStringAsync();
            }
            return str;
        }

        public static async Task<string> PostData(string url, string token, string data)
        {
            string str;
            using (HttpClient httpClient = new HttpClient((HttpMessageHandler)new HttpClientHandler()
            {
                ServerCertificateCustomValidationCallback = (message, cert, chain, error) => true
            }))
            {
                httpClient.DefaultRequestHeaders.Authorization = new AuthenticationHeaderValue("Bearer", token);
                str = await (await httpClient.PostAsync(url, (HttpContent)new StringContent(data, Encoding.UTF8, "application/json"))).Content.ReadAsStringAsync();
            }
            return str;
        }

